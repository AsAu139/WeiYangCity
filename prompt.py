# 3. 结果提取逻辑 (格式检查器)
# ==========================================
def extract_result(raw_response):
    """
    依照分工方案，使用正则表达式精准提取
    """
    # 匹配“答案：”后的所有内容，忽略中间的空格
    match = re.search(re.compile(r"答案[:：]\s*(.*)", re.S), raw_response)

    if match:
        answer = match.group(1).strip()
        # 将答案之前的内容作为推导过程
        reasoning = raw_response[:match.start()].strip()
        return reasoning, answer
    else:
        # 异常处理：如果没有按格式输出，保底提取最后一行
        lines = [l for l in raw_response.split('\n') if l.strip()]
        return raw_response, lines[-1] if lines else ""
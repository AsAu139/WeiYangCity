def get_system_prompt(question_type):
    """
    根据题型动态调整指令。这种“按需分配”的策略能显著提高 AI 的指令遵循度。
    """
    # 基础引导语：融入了你要求的“识别模型->列式->代值”逻辑
    base_instruction = (
        "你是一个精通物理学（力学、电磁学、热学）的智能助手。\n"
        "请严格遵守以下解题流：\n"
        "1. 【物理模型识别】：分析研究对象与物理过程。\n"
        "2. 【列出核心方程】：引用定律名称并书写原始 LaTeX 公式。\n"
        "3. 【分步推导与代入】：进行符号推导后代入数值计算。\n"
        "4. 【最终答案】：必须以“答案：”作为最后一行开头。\n"
        "5. 所有公式使用 LaTeX 渲染，包裹在 $$ 中。\n"
    )

    if "证明题" in question_type:
        specific = "对于证明题，请在推导过程中展示严密的逻辑。在‘答案：’部分，请写出推导的核心逻辑过程与核心表达式，并在最后写出‘证毕’"
    elif "选择题" in question_type:
        specific = "对于选择题，请先分析各选项的正误。在‘答案：’部分，仅输出选项字母（如 A, B, C, D）。"
    else:
        # 涵盖填空题和计算题
        specific = "对于计算和填空题，请在‘答案：’部分输出最终数值及单位。"

    return base_instruction + specific

